angular.module('chatApp')
    //Define the routes for the general entry point of the application
    .config(['$stateProvider',
        function($stateProvider){
            //States for the landing page
            $stateProvider
                .state('landing',{
                    url:'/',
                    views:{
                        'header':{
                            templateUrl:'app/public/header/header.html',
                            controller:'header.Controller'
                        },
                        'leftNav':{},
                        'mainContent':{
                            templateUrl:'app/public/mainContent/login/login.html',
                            controller:'login.Controller'
                        },
                        'rightNav':{}
                    }
                })
                .state('landing.register',{
                    url:'register',
                    views:{
                        'mainContent@':{
                            templateUrl:'app/public/mainContent/register/register.html',
                            controller:'register.Controller'
                        }
                    }
                })
                .state('landing.about',{
                    url:'about',
                    views:{
                        'mainContent@':{
                            templateUrl:'app/public/mainContent/about/about.html'
                        }
                    }
                });
        }
    ]);